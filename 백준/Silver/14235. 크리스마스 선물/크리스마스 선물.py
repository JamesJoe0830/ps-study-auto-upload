import sys
from heapq import heapify, heappop, heappush
input = sys.stdin.readline

N = int(input())
heap = []
heapify(heap)

for i in range(N):
    a = list(map(int,input().split()))
    if a[0] == 0 :
        if heap:
            gift = heappop(heap)
            print(-gift)
        else:
            print(-1)
    else:
        for i in range(1,len(a)):
            heappush(heap,-a[i])

